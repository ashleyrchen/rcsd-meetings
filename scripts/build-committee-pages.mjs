// ---- JSON-LD for a recorded committee meeting (Event + VideoObject) ----
function meetingJsonLd(c, m, lang) {
  const name = `${committeeName(c, lang)}: ${fmtDate(m.date, lang)}`;
  const obj = {
    '@context': 'https://schema.org',
    '@type': 'Event',
    name,
    description: meetingDesc(m, lang) || committeeDesc(c, lang),
    startDate: m.time ? `${m.date}T${m.time}:00-07:00` : m.date,
    eventAttendanceMode: 'https://schema.org/OfflineEventAttendanceMode',
    organizer: { '@type': 'GovernmentOrganization', name: 'Redwood City School District', url: 'https://www.rcsdk8.net/' },
  };
  if (m.location) obj.location = { '@type': 'Place', name: m.location };
  if (m.youtube) {
    obj.recordedIn = {
      '@type': 'VideoObject',
      name,
      description: meetingDesc(m, lang) || committeeDesc(c, lang),
      uploadDate: `${m.date}T00:00:00-07:00`,
      thumbnailUrl: `https://img.youtube.com/vi/${m.youtube}/maxresdefault.jpg`,
      embedUrl: `https://www.youtube.com/embed/${m.youtube}`,
      contentUrl: `https://www.youtube.com/watch?v=${m.youtube}`,
    };
    if (m.durationSeconds) {
      const h = Math.floor(m.durationSeconds / 3600);
      const min = Math.floor((m.durationSeconds % 3600) / 60);
      obj.recordedIn.duration = `PT${h ? h + 'H' : ''}${min}M`;
    }
  }
  return `<script type="application/ld+json">${JSON.stringify(obj)}</script>`;
}

